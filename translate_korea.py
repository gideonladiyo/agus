class TranslateKorea:
    def __init__(self):
        pass
    
    @staticmethod
    def translate_buff_name(data):
        return buff_name[data]
    
    @staticmethod
    def translate_buff_desc(data):
        return buff_desc[data]
    
    @staticmethod
    def translate_weather_name(data):
        return weather_name[data]
    
    @staticmethod
    def translate_weather_desc(data):
        return weather_desc[data]
    
    @staticmethod
    def translate_warzone_desc_korea(text):
        return warzone_desc_korea_translate[text]

    @staticmethod
    def translate_warzone_name_korea(text):
        return warzone_name_korea_translate[text]
    
    @staticmethod
    def translate_korea_warzone(data):
        print(data["name"])
        data["name"] = TranslateKorea.translate_warzone_name_korea(data["name"])
        data["description"] = TranslateKorea.translate_warzone_desc_korea(data["description"])
        for buff in data["buffs"]:
            buff["name"] = TranslateKorea.translate_buff_name(buff["name"])
            buff["description"] = TranslateKorea.translate_buff_desc(buff["description"])
        
        for weather in data["weathers"]:
            weather["name"] = TranslateKorea.translate_weather_name(weather["name"])
            weather["description"] = TranslateKorea.translate_weather_desc(weather["description"])
        
        return data